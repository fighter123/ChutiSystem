package com.lhp.unit;

public class Return
{
	public Return(String timu, int id)
	{
		super();
		this.timu = timu;
		this.id = id;
	}
	
	private String timu;
	private int id;
	public String getTimu()
	{
		return timu;
	}
	public void setTimu(String timu)
	{
		this.timu = timu;
	}
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	
	
	
}
